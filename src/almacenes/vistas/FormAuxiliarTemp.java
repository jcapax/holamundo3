    private void agregar(){
        cantidad = Integer.valueOf(jtxtCantidad.getText().toString());
        valorTotal = Double.valueOf(jtxtValorTotal.getText().toString());
        valorUnitario = valorTotal / cantidad;
        
        Temporal temp = new Temporal();

        String nombreProducto = jlDescripcionProducto.getText();
        String simbolo = "";
        String tipoValor = "N";// normal
        
        temp.setCantidad(cantidad);
        temp.setIdProducto(idProducto);
        temp.setIdUnidadMedida(idUnidadMedida);
        temp.setNombreProducto(nombreProducto);
        temp.setSimbolo(simbolo);
        temp.setTipoValor(tipoValor);
        temp.setValorTotal(valorTotal);
        temp.setValorUnitario(valorUnitario);
        
        TemporalDAO tempDAO = new TemporalDAOImpl(connectionDB);
        tempDAO.insertarProductoTemp(temp);               
        dispose();
    }

